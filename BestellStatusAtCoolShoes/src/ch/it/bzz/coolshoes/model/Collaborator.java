public class Collaborator {

  int mid;
  String name;
  String forename;
  
  
  public int getMid() {
    return mid;
  }
  public void setMid(int mid) {
    this.mid = mid;
  }
  public String getName() {
    return name;
  }
  public void setName(String name) {
    this.name = name;
  }
  public String getForename() {
    return forename;
  }
  public void setForename(String forename) {
    this.forename = forename;
  }
}
