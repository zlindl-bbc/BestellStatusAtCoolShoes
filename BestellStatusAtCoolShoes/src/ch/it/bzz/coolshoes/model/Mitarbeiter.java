public class Mitarbeiter {

  private int MID;
  private String MAName;
  private String MAVorname;
  private String MAPasswort;

  public int getMID() {
    return MID;
  }

  public void setMID(int mID) {
    MID = mID;
  }

  public String getMAName() {
    return MAName;
  }

  public void setMAName(String mAName) {
    MAName = mAName;
  }

  public String getMAVorname() {
    return MAVorname;
  }

  public void setMAVorname(String mAVorname) {
    MAVorname = mAVorname;
  }

  public String getMAPasswort() {
    return MAPasswort;
  }

  public void setMAPasswort(String mAPasswort) {
    MAPasswort = mAPasswort;
  }


}
